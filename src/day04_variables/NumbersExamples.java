package day04_variables;

public class NumbersExamples {

    public static void main(String[] args) {

        // byte

        byte dateNumber = 31;
        System.out.println( dateNumber);





        // short

        short numberTwo = 200;
        System.out.println(numberTwo);

        // int
        int intNumber; // declare an int variable called intNumber
        intNumber = 10000; // assign 10000 to my inNumber variable
        System.out.println(intNumber);



        // long
        long fourthNumber = 2344324;
        System.out.println(fourthNumber);

        long bigNumber = 3_000_000_000L; // underscore makes the value readable
        System.out.println(bigNumber);

        int version = 4;
        System.out.println(version);
        version =6;
        System.out.println(version);

        int num3 = 545;
        System.out.println(num3);

    }

}

package day24_loops;

public class MultiplicationPractice {
    public static void main(String[] args) {

        int number = 10;

        for(int i = 1; i < number; i++){
            System.out.println(i * number);
        }
        System.out.println();

        int num = 20;
        for (int d = 1; d < num; d++  ){
            System.out.println(d * number);
        }

    }
}

package day24_loops;

public class MultiplicationNumber {

    public static void main(String[] args) {
        int number = 9;

        for (int i = 1; i <=10; i++){
            System.out.println(number + " x " + i + " = " + (number*i));
        }

        System.out.println();

        int num = 9;
        for(int s = 1; s <= 20; s++){
            System.out.println(s * num);
        }



    }
}

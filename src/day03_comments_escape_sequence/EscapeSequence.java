package day03_comments_escape_sequence;

public class EscapeSequence {

    public static void main(String[] args) {


        System.out.println("I love \"java\" programming");//

        System.out.println(" I like to \'cook\'\\eat and \' sleep\'"); // this puts a single quote around

        System.out.println("Tomorrow is Friday.\nIt. is a day off"); // the n make the statement go to next line

        System.out.println("\t\tmessage"); // \t will give a tab in in the output (5 spaces)


    }
}
